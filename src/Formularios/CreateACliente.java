    private void jButtonRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarseActionPerformed
        if(this.txtDNI.getText().length()!=0){
            String nombre = this.txtNombre.getText();
            String APaterno = this.jTextFieldPaterno.getText();
            String AMaterno = this.jTextFieldMaterno.getText();
            String dni = this.txtDNI.getText();
            int edad = Integer.parseInt(this.jTextFieldEdad.getText());
            String sexo = (String)this.jComboBoxSexo.getSelectedItem();
            String numero = this.jTextFieldNumeroTelf.getText();
            String correo = this.jTextFieldCorreo.getText();
            String user_ide = this.txtUser.getText();
            String password = this.txtPassword.getText();
        
            Cliente cl = new Cliente(nombre, APaterno, AMaterno, dni, edad, sexo, numero, correo, user_ide, password);
       
            if(Sistema.usuarios.agregarUsuario(cl)){
                        JOptionPane.showMessageDialog(this, "REGISTRADO!");
                        Sistema.usuario = Sistema.usuarios.validar(this.txtUser.getText(), this.txtPassword.getText());
                        InicioCliente i= new InicioCliente();
                        i.setVisible(true);
                        i.setLocationRelativeTo(null);
                        this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Error!");
        }
    }//GEN-LAST:event_jButtonRegistrarseActionPerformed

