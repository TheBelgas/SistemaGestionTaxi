    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        Usuario user;
        user=Sistema.usuarios.getUsuario(this.jTextFielDniBuscar.getText());
        if(user!=null&&user instanceof Cliente){
            this.jTextFieldNombreSelect.setText(user.getNombre());
            this.jTextFieldDniSelect.setText(user.getDni());
            this.jTextFieldUserIdeSelect.setText(user.getId_user());
        }else{
            JOptionPane.showMessageDialog(null, "No se ha encontrado cliente");
        }
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        Icon imagen = new ImageIcon(getClass().getResource("/Recursos/BotonEliminar.png"));
        String[] opciones = {"SÃ­","Cancelar"};
        
        int i = JOptionPane.showInternalOptionDialog(null, "Â¿EstÃ¡ seguro que desea ELIMINAR este CLIENTE de forma PERMANENTE?",
                "Elija una opciÃ³n", WIDTH, HEIGHT, imagen, opciones, EXIT_ON_CLOSE);
        if(i==0){
            if(Sistema.usuarios.eliminarUsuario(this.jTextFieldDniSelect.getText())){
                JOptionPane.showMessageDialog(null, "Eliminado");
                this.cargarTabla();
                limpiarDatos();
                editarCliente(false);
            }else{
                JOptionPane.showMessageDialog(null, "Error");
            }
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jTClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTClientesMouseClicked
        int seleccionar = jTClientes.rowAtPoint(evt.getPoint());
        this.jTextFieldDniSelect.setText(String.valueOf(this.jTClientes.getValueAt(seleccionar,0 )));
        this.jTextFieldNombreSelect.setText(String.valueOf(this.jTClientes.getValueAt(seleccionar,1 )));
        this.jTextFieldUserIdeSelect.setText(String.valueOf(this.jTClientes.getValueAt(seleccionar, 2)));
    }//GEN-LAST:event_jTClientesMouseClicked

