    private void jButtonGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarCambiosActionPerformed
        Cliente clienteActual = (Cliente) Sistema.usuarios.getUsuario(this.jTextFieldDniSelect.getText());
        Cliente clienteMod = new Cliente(this.jTextFieldNombreSelect.getText(),
                this.jTextFieldDniSelect.getText() , this.jTextFieldUserIdeSelect.getText(), clienteActual.getPassword());
        if(Sistema.usuarios.modificarUsuario(clienteActual.getDni(), clienteMod)){
            JOptionPane.showMessageDialog(null,"Modificado");
            cargarTabla();
            editarCliente(false);
        }else{
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButtonGuardarCambiosActionPerformed

